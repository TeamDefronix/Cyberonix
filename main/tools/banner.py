def wrap_text(text, width=80):
    wrapped_text = []
    lines = text.split(" ")
    line = ""
    for word in lines:
        if len(line) + len(word) <= width:
            line += word + " "
        else:
            wrapped_text.append(line)
            line = word + " "
    wrapped_text.append(line)
    return "\n".join(wrapped_text)
